# Generate charts/joi/values.yaml from the jsonnet-oci-images manifest
# (libraries.json on stdin or $1) — the single source of truth the JOI build
# pipeline produces. Run by .github/workflows/sync-joi.yml so a new upstream
# library flows into the chart with zero hand-editing. Image tags are always
# `latest`; the consumer selects a version in the import path. A curated set is
# enabled by default; everything else ships disabled.
#
#   curl -fsSL <raw libraries.json> | hack/gen-joi-values.sh > charts/joi/values.yaml
set -euo pipefail
manifest="${1:-/dev/stdin}"

# Default-enabled (dependency-closed common set). Everything else ships disabled.
enabled_set=" grafonnet xtd docsonnet k8s-libsonnet "

cat <<'EOF'
# SPDX-FileCopyrightText: The helm-charts Authors
# SPDX-License-Identifier: 0BSD
#
# GENERATED by hack/gen-joi-values.sh from the jsonnet-oci-images manifest.
# Do not hand-edit the `libraries` map; it is regenerated by sync-joi.yml. Only
# the curated default-enabled set and the non-library settings below are policy.

# Namespace the OCIRepository + JsonnetLibrary objects land in (release ns when empty).
namespace: ""

# Internal registry mirror for the JOI OCI artifacts (rewrites the registry host).
registryMirror: ""

# dockerconfigjson Secret for authenticated (private-mirror) pulls.
imagePullSecret: ""

# @schema
# type: string
# pattern: ^[0-9]+(m|h|s)$
# @schema
# OCIRepository re-pull cadence (a moved `latest` tag is picked up here).
interval: 60m

# One entry per JOI image. Pick the LIBRARY version in the import path, e.g.
# `import 'github.com/jsonnet-libs/k8s-libsonnet/1.34/main.libsonnet'` or
# `.../latest/main.libsonnet`. `path: ""` exposes the whole vendor tree. The
# `tag` defaults to `latest` (moving); set it to a dated JOI snapshot
# (e.g. `2026.6.16`) to pin the image for reproducible pulls.
#
# @schema
# additionalProperties: true
# @schema
libraries:
EOF

# Each manifest entry -> a library block; image is ghcr.io/metio/joi-<org>-<repo>.
jq -r --arg en "$enabled_set" '
  sort_by(.name)[]
  | (" " + .name + " ") as $needle
  | (if ($en | contains($needle)) then "true" else "false" end) as $e
  | ("[" + ((.closure // []) | join(", ")) + "]") as $clo
  | "  \(.name):\n    enabled: \($e)\n    image: ghcr.io/metio/joi-\(.org)-\(.repo)\n    tag: latest\n    path: \"\"\n    # libraries this one imports; auto-enabled with it\n    closure: \($clo)"
' "$manifest"
